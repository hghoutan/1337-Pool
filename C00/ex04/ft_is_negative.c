#include <unistd.h>

void	ft_is_negative(int n)
{
	char	ng;
	char	p;

	ng = 'N';
	p = 'P';
	if (n < 0)
	{
		write(1, &ng, 1);
	}
	else
	{
		write(1, &p, 1);
	}
}
