#include <unistd.h>

void	ft_print_comb2(void)
{
	int		n;
	int		m;
	char	num1[2];

	n = 0;
	while (n < 100)
	{
		m = n + 1;
		while (m < 100)
		{
			num1[0] = (n / 10) + '0';
			num1[1] = (n % 10) + '0';
			write(1, num1, 2);
			write(1, " ", 1);
			num1[0] = (m / 10) + '0';
			num1[1] = (m % 10) + '0';
			write(1, num1, 2);
			if (!(n == 98 && m == 99))
				write(1, ", ", 2);
			m++;
		}
		n++;
	}
}
